# ---------------- Recolección de información básica ----------------

# Pide al usuario que ingrese su nombre
name = input("Ingresa tu nombre: ")

# Pide al usuario que ingrese su edad (como texto)
age = input("Ingresa tu edad: ")

# Muestra un saludo personalizado con el nombre y edad
print(f"Hola {name}, ¡ya tienes {age}!")

# ---------------- Preguntas divertidas ----------------

# Pide la comida favorita del usuario
favFood = input("¿Cuál es tu comida favorita? ")

# Pide el color favorito del usuario
favColor = input("¿Cuál es tu color favorito? ")

# Muestra una frase graciosa uniendo comida y color
print(f"¡Vaya! {favFood} {favColor} suena gracioso")

# ---------------- Operaciones con números ----------------

# Pide al usuario un número, y lo convierte a entero
number = int(input("Ahora, ingresa un número: "))

# Calcula el doble del número
doble = number * 2

# Calcula el triple del número
triple = number * 3

# Muestra los resultados del doble y triple
print(f"El doble de tu número es {doble}, y el triple es {triple}")
